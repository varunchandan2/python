# Ask user for three integers and accept it as a string
number = input("Please enter a three digit number :")
result = 0

# These variables will store each number individually and accept it as integer 
digit1 =  number[0]
digit1 = int(digit1)
digit2 =  number[1]
digit2 = int(digit2)
digit3 =  number[2]
digit3 = int(digit3)


# Calculating the sum of the numbers
result = digit1 + digit2 + digit3


# Display the sum of the numbers
print(" The sum of the number entered is",result)



