
#Accept length from the user and store as a integer
length = input("Enter length of the box: ")
length = int(length)
#Accept width from the user and store as a integer
width = input("Enter width of the box: ")
width = int(width)
#Accept height from the user and store as a integer
height = input("Enter height of the box: ")
height = int(height)
# Using the formula to calculate the volume and store it as volume
volume = length * width * height
#Output the final result
print("The volume of the box is",volume)

               
